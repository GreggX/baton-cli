/**
 * (Re)generate every fixture deterministically. Existing generated trees are replaced
 * so replayed growth turns never leak into the next test run.
 */
export function generateFixtures(repoRoot: string = fixtureRepoRoot()): void {
  uuidCounter = 0;
  const fixturesDir = join(repoRoot, 'tests', 'fixtures');
  const projectsDir = join(fixturesDir, 'claude', 'projects');
  const growthDir = join(fixturesDir, 'growth');

  rmSync(projectsDir, { recursive: true, force: true });
  rmSync(growthDir, { recursive: true, force: true });
  mkdirSync(projectsDir, { recursive: true });
  mkdirSync(growthDir, { recursive: true });

  const workspaceNames = [
    'ws-green',
    'ws-yellow',
    'ws-orange',
    'ws-red',
    'ws-growing',
    'ws-empty',
    'ws-decisions',
    'ws-no-matches',
    'ws-bad-config',
  ];
  for (const name of workspaceNames) {
    const wsDir = join(fixturesDir, name);
    mkdirSync(wsDir, { recursive: true });
    writeFileSync(join(wsDir, '.gitkeep'), '');
  }

  // ws-bad-config: orange (60) below yellow (65) — ordering violation (FR-003).
  writeFileSync(
    join(fixturesDir, 'ws-bad-config', 'baton.config.json'),
    `${JSON.stringify({ thresholds: { yellow: 65, orange: 60, red: 75 } }, null, 2)}\n`,
  );

  for (const spec of buildTranscripts()) {
    const wsPath = join(fixturesDir, spec.workspaceName);
    const encoded = encodeWorkspacePathForFixtures(wsPath);
    const sessionDir = join(projectsDir, encoded);
    mkdirSync(sessionDir, { recursive: true });
    const transcriptPath = join(sessionDir, `${spec.sessionId}.jsonl`);
    const content = spec.lines === null ? '' : `${spec.lines(wsPath).join('\n')}\n`;
    writeFileSync(transcriptPath, content);
    // Pristine copy for append-turns.sh reset, for the replayable fixtures.
    if (spec.workspaceName === 'ws-growing' || spec.workspaceName === 'ws-decisions') {
      writeFileSync(join(growthDir, `${spec.workspaceName}.base.jsonl`), content);
    }
  }

  for (const [workspaceName, build] of Object.entries(buildGrowthTurns())) {
    const wsPath = join(fixturesDir, workspaceName);
    writeFileSync(
      join(growthDir, `${workspaceName}.turns.jsonl`),
      `${build(wsPath).join('\n')}\n`,
    );
  }
}

// CLI entry: `npx tsx scripts/fixtures/generate-fixtures.ts`
if (process.argv[1] !== undefined) {
  const invoked = resolve(process.argv[1]);
  const self = fileURLToPath(import.meta.url);
  if (invoked === self) {
    generateFixtures();
    console.error(`fixtures generated under ${join(fixtureRepoRoot(), 'tests', 'fixtures')}`);
  }
}
