/** Encode a workspace path the way Claude Code does: absolute path, "/" → "-". */
export function encodeWorkspacePathForFixtures(absoluteWorkspace: string): string {
  return absoluteWorkspace.replaceAll('/', '-');
}

/** Repository root (this file lives at scripts/fixtures/). */
export function fixtureRepoRoot(): string {
  return resolve(dirname(fileURLToPath(import.meta.url)), '..', '..');
}

