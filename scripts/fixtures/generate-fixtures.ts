interface Usage {
  input_tokens: number;
  cache_creation_input_tokens: number;
  cache_read_input_tokens: number;
  output_tokens: number;
}

function usage(
  input: number,
  cacheCreation: number,
  cacheRead: number,
  output: number,
): Usage {
  return {
    input_tokens: input,
    cache_creation_input_tokens: cacheCreation,
    cache_read_input_tokens: cacheRead,
    output_tokens: output,
  };
}

let uuidCounter = 0;
function nextUuid(): string {
  uuidCounter += 1;
  return `00000000-0000-4000-8000-${String(uuidCounter).padStart(12, '0')}`;
}

function summaryLine(text: string): string {
  return JSON.stringify({ type: 'summary', summary: text, leafUuid: nextUuid() });
}

function userLine(sessionId: string, cwd: string, timestamp: string, text: string): string {
  return JSON.stringify({
    type: 'user',
    uuid: nextUuid(),
    parentUuid: null,
    sessionId,
    timestamp,
    cwd,
    version: '2.0.0',
    gitBranch: 'main',
    message: { role: 'user', content: [{ type: 'text', text }] },
  });
}

function assistantLine(
  sessionId: string,
  cwd: string,
  timestamp: string,
  text: string,
  model: string,
  usageBlock: Usage | null,
): string {
  const message: Record<string, unknown> = {
    id: `msg_fixture_${String(uuidCounter).padStart(4, '0')}`,
    type: 'message',
    role: 'assistant',
    model,
    content: [{ type: 'text', text }],
    stop_reason: 'end_turn',
    stop_sequence: null,
  };
  if (usageBlock !== null) {
    message['usage'] = { ...usageBlock, service_tier: 'standard' };
  }
  return JSON.stringify({
    type: 'assistant',
    uuid: nextUuid(),
    parentUuid: null,
    sessionId,
    timestamp,
    cwd,
    version: '2.0.0',
    gitBranch: 'main',
    message,
  });
}

