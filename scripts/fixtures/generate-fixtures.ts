// T016 — Synthetic fixture workspaces with Claude Code-shaped JSONL transcripts.
//
// Layout produced (all content is deterministic — same bytes on every run):
//
//   tests/fixtures/ws-<name>/                          nine fixture workspaces
//   tests/fixtures/claude/projects/<encoded-ws>/       transcripts, one <session-id>.jsonl
//   tests/fixtures/growth/<ws-name>.base.jsonl         pristine copy for replay reset
//   tests/fixtures/growth/<ws-name>.turns.jsonl        turns replayed by append-turns.sh
//
// <encoded-ws> is the workspace's absolute path with "/" replaced by "-", exactly the
// way Claude Code encodes project directories under ~/.claude/projects. Because the
// encoding embeds the machine-specific absolute path, fixtures are (re)generated by
// this script rather than committed under a hard-coded encoded name. Tests call
// `generateFixtures()` in their setup; `npx tsx scripts/fixtures/generate-fixtures.ts`
// does the same from the shell.
//
// Fixture percentages assume the 200k context window of the known model
// `claude-sonnet-4-5` (the latest assistant entry's message.usage total is the
// context footprint):
//
//   ws-green      50,000 tokens ≈ 25%   exact
//   ws-yellow     90,400 tokens ≈ 45.2% exact (+ malformed lines that must be skipped,
//                 + three older sessions: unknown model, missing usage, [1m] window)
//   ws-orange    136,000 tokens ≈ 68%   exact
//   ws-red       160,000 tokens ≈ 80%   exact (task/question phrases for handoff)
//   ws-growing    70,000 tokens ≈ 35%   replayable to 136,000 ≈ 68% (multi-band jump)
//   ws-empty      zero-byte transcript  → reading unavailable, never a fake zone
//   ws-decisions  50,000 tokens ≈ 25%   decision/conclusion phrases, replayable to 68%
//   ws-no-matches 30,000 tokens ≈ 15%   deliberately bland: NO heuristic phrases —
//                 keep it free of decision/conclusion/constraint/result/task/question
//                 trigger phrases and question marks
//   ws-bad-config 90,000 tokens ≈ 45%   workspace has an invalid baton.config.json
//                 (orange 60 < yellow 65 → falls back to defaults with named error)
import { mkdirSync, rmSync, writeFileSync } from 'node:fs';
